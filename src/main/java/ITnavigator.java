import java.util.ArrayList;
import java.util.List;
import java.util.Scanner;

public class ITnavigator {
    private Scanner scanner = new Scanner(System.in);

    public ITnavigator() {
        start();
    }

    private void start() {
        String choice;
        int gradePoint = 0, n = 0;
        List<String> questions = new ArrayList<>();
        questions.add("Какой тип задач тебе больше нравится?\n" +
                "a) Веб-разработка (сайты, фронтенд, бэкенд)\n" +
                "b) Разработка игр (2D, 3D, движки)\n" +
                "c) Искусственный интеллект и анализ данных\n" +
                "d) Системное программирование (операционные системы, драйверы)");
        questions.add("Как ты выбираешь инструменты для работы?\n" +
                "a) Использую популярные фреймворки и библиотеки\n" +
                "b) Предпочитаю разрабатывать свои инструменты\n" +
                "c) Работаю с тем, что лучше всего подходит под задачу\n" +
                "d) Люблю минимализм, пишу на низкоуровневых языках");
        questions.add("Как ты относишься к визуальной составляющей?\n" +
                "a) Люблю красивые интерфейсы, важен дизайн\n" +
                "b) Главное — функциональность, дизайн вторичен\n" +
                "c) Обожаю 3D-графику и анимации\n" +
                "d) Упор на оптимизацию, визуал не главное");
        questions.add("Как ты предпочитаешь работать?\n" +
                "a) Командная разработка, Agile, SCRUMа\n" +
                "b) Индивидуальная работа, проекты для души\n" +
                "c) Исследовательская деятельность, сложные алгоритмы\n" +
                "d) Написание высокопроизводительного кода");
        questions.add("Какой твой любимый язык программирования?\n" +
                "a) JavaScript/Python — 3 балла" +
                "b) C#/C++\n" +
                "c) R/Python (AI, анализ данных)\n" +
                "d) C/Rust (низкоуровневое программирование)\n" +
                "e) Java/Kotlin (универсальный язык, мобильная и корпоративная разработка)");
        questions.add("Какой стиль написания кода тебе ближе?\n" +
                "a) Чистый, структурированный, придерживаюсь стандартов\n" +
                "b) Главное, чтобы работало, а стиль неважен\n" +
                "c) Люблю писать креативный код с нестандартными решениями\n" +
                "d) Минимализм: меньше кода, больше эффективности");
        questions.add("Как ты относишься к тестированию?\n" +
                "a) Обязательно пишу тесты, это важно\n" +
                "b) Пишу тесты, но только когда нужно\n" +
                "c) Тестирование — пустая трата времени, код должен работать сразу\n" +
                "d) Оставляю тестирование на других, я просто пишу код\n");
        questions.add("Как ты учишься новым технологиям?\n" +
                "a) Читаю документацию и книги\n" +
                "b) Смотрю видеоуроки и прохожу курсы\n" +
                "c) Пробую всё на практике, учусь на ошибках\n" +
                "d) Жду, пока технология станет мейнстримом, а потом разбираюсь");
        questions.add("Какой у тебя подход к решению сложных задач?\n" +
                "a) Разбираю задачу на маленькие части и решаю поэтапно\n" +
                "b) Ищу готовые решения в интернете и адаптирую их\n" +
                "c) Экспериментирую, пока не найду оптимальный вариант\n" +
                "d) Сразу пытаюсь решить в голове, а потом пишу код");
        questions.add("Как ты относишься к новым языкам программирования?\n" +
                "a) Всегда пробую новые технологии и тренды\n" +
                "b) Использую только проверенные временем языки\n" +
                "c) Изучаю новые языки, если они действительно полезны для проекта\n" +
                "d) Стараюсь избегать новых языков, если старые меня устраивают");
        int totalQuestions = questions.size();

        System.out.print("Тест: Твой путь в программировании\n" +
                "\uD83D\uDE80 Добро пожаловать в тест, который поможет определить, какое направление\n" +
                "в программировании подходит тебе больше всего! Независимо от того, новичок ты или уже\n" +
                "имеешь опыт, этот тест подскажет, где твои сильные стороны и куда стоит двигаться дальше.\n" +
                "Отвечай на вопросы, набирай баллы и в конце узнай, кем ты можешь стать в мире IT! \uD83D\uDCBB✨" +
                "\n\n1 - начать тест\nВвод: ");
        choice = scanner.nextLine();
        if (choice.equals("1")) {
            for (int i = 0; i < totalQuestions; i++) {
                System.out.print("(" + (n + 1) + "/" + totalQuestions + "). " + questions.get(i) + "\n(1 - выход) Ввод: ");
                choice = scanner.nextLine();
                System.out.println();
                n++;

                switch (choice) {
                    case "a": gradePoint += 1; break;
                    case "b": gradePoint += 2; break;
                    case "c": gradePoint += 3; break;
                    case "d": gradePoint += 4; break;
                    case "e": gradePoint += 5; break;
                    case "1":
                        System.out.println("Выход из теста.");
                        return;
                    default:
                        System.out.println("Некорректный ввод, попробуйте снова.");
                        i--;
                        n--;
                        break;
                }
            }

            System.out.println("\nРезультат теста:");
            if (gradePoint >= 10 && gradePoint <= 17) {
                System.out.println("Веб-разработчик\nТы любишь создавать сайты, веб-приложения и фронтенд-разработку.");
            } else if (gradePoint >= 18 && gradePoint <= 25) {
                System.out.println("Игровой разработчик\nТы обожаешь создавать игры, работать с движками и заниматься анимацией.");
            } else if (gradePoint >= 26 && gradePoint <= 32) {
                System.out.println("Специалист по AI и данным\nТы любишь анализ данных, машинное обучение и работу с большими объёмами информации.");
            } else if (gradePoint >= 33) {
                System.out.println("Системный программист\nТы работаешь с низкоуровневым программированием, оптимизацией и архитектурой ПО.");
            } else {
                System.out.println("Результат не определён. Попробуйте ещё раз.");
            }
        } else {
            System.out.println("Выход");
        }
    }
}
